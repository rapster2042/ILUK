# Fix Small Wins Categories and Data

## Purpose
The previous migration failed because the CHECK constraint on the category column
prevented inserting data with the new category names. This migration:
1. Drops the old CHECK constraint
2. Clears existing data
3. Adds new CHECK constraint with correct categories
4. Inserts all 1000 tasks with correct categories

## Categories
- self-care (200 tasks)
- financial (200 tasks)
- household (200 tasks)
- social (200 tasks)
- learning (100 tasks)
- health (100 tasks)
