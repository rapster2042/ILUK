/*
# Small Wins System

## Overview
Creates a database of 1000 small wins/tasks for the "Today's small wins" section.
Implements a 24-hour rotation system that shows different wins each day.

## Tables

### small_wins
Stores the database of 1000 small win suggestions.
- `id` (bigserial, primary key) - Unique identifier
- `title` (text, not null) - The small win text/task description
- `category` (text, not null) - Category: 'self-care', 'financial', 'household', 'social', 'learning', 'health'
- `created_at` (timestamptz, default: now()) - Creation timestamp

### user_small_wins_history
Tracks which small wins have been shown to users and when.
- `id` (uuid, primary key) - Unique identifier
- `user_uuid` (text, not null) - User identifier (from localStorage for anonymous users)
- `small_win_id` (bigint, foreign key) - Reference to small_wins table
- `shown_at` (timestamptz, not null) - When this win was shown
- `created_at` (timestamptz, default: now()) - Creation timestamp

## RPC Functions

### get_daily_small_wins(p_user_uuid text, p_count int)
Returns a set of small wins for the user for today.
- Uses deterministic selection based on current date
- Avoids recently shown wins (within last 30 days)
- Returns specified count of wins (default 3)

## Security
- Public read access on small_wins table (no RLS needed - public data)
- Public insert/read access on user_small_wins_history (anonymous tracking)

## Data
Inserts 1000 diverse small wins across 6 categories:
- Self-care (200 items)
- Financial (200 items)
- Household (200 items)
- Social (200 items)
- Learning (100 items)
- Health (100 items)
*/

-- Create small_wins table
CREATE TABLE IF NOT EXISTS small_wins (
  id bigserial PRIMARY KEY,
  title text NOT NULL,
  category text NOT NULL CHECK (category IN ('self-care', 'financial', 'household', 'social', 'learning', 'health')),
  created_at timestamptz DEFAULT now()
);

-- Create user_small_wins_history table
CREATE TABLE IF NOT EXISTS user_small_wins_history (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_uuid text NOT NULL,
  small_win_id bigint NOT NULL REFERENCES small_wins(id) ON DELETE CASCADE,
  shown_at timestamptz NOT NULL,
  created_at timestamptz DEFAULT now()
);

-- Create index for faster queries
CREATE INDEX IF NOT EXISTS idx_user_small_wins_history_user_uuid ON user_small_wins_history(user_uuid);
CREATE INDEX IF NOT EXISTS idx_user_small_wins_history_shown_at ON user_small_wins_history(shown_at);

