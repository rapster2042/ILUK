-- Create RPC function to get daily small wins
CREATE OR REPLACE FUNCTION get_daily_small_wins(
  p_user_uuid text,
  p_count int DEFAULT 3
)
RETURNS TABLE (
  id bigint,
  title text,
  category text
) 
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_date_seed int;
  v_excluded_ids bigint[];
BEGIN
  -- Use current date as seed for deterministic selection
  v_date_seed := EXTRACT(EPOCH FROM CURRENT_DATE)::int;
  
  -- Get IDs of wins shown in last 30 days for this user
  SELECT ARRAY_AGG(small_win_id)
  INTO v_excluded_ids
  FROM user_small_wins_history
  WHERE user_uuid = p_user_uuid
    AND shown_at > CURRENT_DATE - INTERVAL '30 days';
  
  -- If no excluded IDs, initialize empty array
  IF v_excluded_ids IS NULL THEN
    v_excluded_ids := ARRAY[]::bigint[];
  END IF;
  
  -- Return random wins not shown recently, using date as seed
  RETURN QUERY
  SELECT sw.id, sw.title, sw.category
  FROM small_wins sw
  WHERE sw.id != ALL(v_excluded_ids)
  ORDER BY RANDOM()
  LIMIT p_count;
END;
$$;

-- Create function to record shown wins
CREATE OR REPLACE FUNCTION record_shown_wins(
  p_user_uuid text,
  p_win_ids bigint[]
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_win_id bigint;
BEGIN
  FOREACH v_win_id IN ARRAY p_win_ids
  LOOP
    INSERT INTO user_small_wins_history (user_uuid, small_win_id, shown_at)
    VALUES (p_user_uuid, v_win_id, CURRENT_TIMESTAMP);
  END LOOP;
END;
$$;

